package com.delta;

public class DocNum {

	private String X_STATUS;
	private String X_DOC_NUMBER;
	private String X_MESSAGE;
	public String getX_STATUS() {
		return X_STATUS;
	}
	public void setX_STATUS(String xSTATUS) {
		X_STATUS = xSTATUS;
	}
	public String getX_DOC_NUMBER() {
		return X_DOC_NUMBER;
	}
	public void setX_DOC_NUMBER(String xDOCNUMBER) {
		X_DOC_NUMBER = xDOCNUMBER;
	}
	public String getX_MESSAGE() {
		return X_MESSAGE;
	}
	public void setX_MESSAGE(String xMESSAGE) {
		X_MESSAGE = xMESSAGE;
	}
	
	
	
	
}
